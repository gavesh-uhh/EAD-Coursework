    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedIndex = searchList.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedName = (String) listModel.getElementAt(selectedIndex);
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Are you sure you want to delete " + selectedName + "?",
                    "Confirm Deletion", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                int subjectId = DatabaseHelper.getSubjectIdByName(selectedName);
                boolean deleted = DatabaseHelper.deleteSubject(subjectId);
                if (deleted) {
                    listModel.remove(selectedIndex);
                    JOptionPane.showMessageDialog(this, selectedName + " deleted successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "This teacher is currently enrolled.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a teacher to delete.");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void addSearchTerm(String message) {
        DefaultListModel defModel = (DefaultListModel) searchList.getModel();
        defModel.addElement(message);
    }

    private void loadAllSubjects() {
        listModel.clear();
        ArrayList<Subject> allTeachers = DatabaseHelper.getAllSubjects();
        if (allTeachers.isEmpty()) {
            listModel.addElement("No teachers available in the database.");
        } else {
            allTeachers.forEach(teacher -> {
                listModel.addElement(teacher.getSubjectName());
            });
        }
    }

