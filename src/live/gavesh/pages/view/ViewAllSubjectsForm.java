    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        listModel.clear();

        String searchTerm = this.searchField.getText().trim().toLowerCase();

        ArrayList<Subject> allSubjects = DatabaseHelper.getAllSubjects();

        ArrayList<Subject> filteredSubjects = new ArrayList<>();

        allSubjects.forEach((t) -> {
            if (t.getSubjectName().toLowerCase().contains(searchTerm)) {
                filteredSubjects.add(t);
            }
        });

        if (filteredSubjects.isEmpty()) {
            listModel.addElement("No matching teachers found.");
        } else {
            filteredSubjects.forEach(subject -> {
                listModel.addElement(subject.getSubjectName());
            });
        }
    }//GEN-LAST:event_searchButtonActionPerformed

