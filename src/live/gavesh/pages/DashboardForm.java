    private void loadStudents() {
        String searchString = this.searchField.getText();
        DefaultListModel defModel = (DefaultListModel) searchList.getModel();
        defModel.clear();
        DatabaseHelper.getAllStudents().forEach((t) -> {
            if (t.getFullName().startsWith(searchString) || searchString.isEmpty()) {
                defModel.addElement(t.getFullName());
            }
        });
    }

