    private void deleteStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStudentButtonActionPerformed
        Object selectedItem = searchList.getSelectedValue();
        if (selectedItem.toString().isEmpty()) {
            return;
        }
        Student student = DatabaseHelper.getStudentByName(selectedItem.toString());
        DatabaseHelper.deleteStudent(Integer.parseInt(student.getStudentId()));
        int choice = JOptionPane.showConfirmDialog(
                null,
                "Are you sure you want to delete the student: " + student.getFullName() + " (ID: " + student.getStudentId() + ")?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        if (choice == JOptionPane.YES_OPTION) {
            try {
                DatabaseHelper.deleteStudent(Integer.parseInt(student.getStudentId()));
                JOptionPane.showMessageDialog(null, "Student '" + student.getFullName() + "' deleted successfully.", "Deletion Successful", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error: Invalid student ID format for deletion. " + e.getMessage(), "Deletion Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "An error occurred during student deletion: " + e.getMessage(), "Deletion Error", JOptionPane.ERROR_MESSAGE);
            } finally {
                loadStudents();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Student deletion cancelled.", "Deletion Cancelled", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_deleteStudentButtonActionPerformed

